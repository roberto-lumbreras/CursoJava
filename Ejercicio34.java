public class Ejercicio34 {
    public static void main(String[] args) {
        System.out.println("Valores limite de int");
        System.out.println(Integer.MAX_VALUE);
        System.out.println(Integer.MIN_VALUE);
        System.out.println("Valores limite de double");
        System.out.println(Double.MAX_VALUE);
        System.out.println(-Double.MAX_VALUE);
        System.out.println("Valores limite de char");
        System.out.println(Integer.toHexString(Character.MAX_VALUE));
        System.out.println(Integer.toHexString(Character.MIN_VALUE));

        
    }
}
